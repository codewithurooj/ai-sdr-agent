/**
 * Returns the next actionable status for a given email.
 * Used to determine which action button to show.
 */
export const getNextAction = (
  status: EmailStatus
): { label: string; action: "approve" | "send" | "discard" } | null => {
  if (status === "draft")      return { label: "Approve",  action: "approve" };
  if (status === "scheduled")  return { label: "Send Now", action: "send" };
  return null;
};
