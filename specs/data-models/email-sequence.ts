// Email sequence data models
// Each Prospect gets exactly 3 emails: Day 1, Day 4, Day 7.
// All emails start as "draft" — human must approve before they are scheduled.

// ── Enums ──────────────────────────────────────────────────────────────────

export type EmailStatus =
  | "draft"      // generated by Writer agent, awaiting human review
  | "scheduled"  // approved by human, will send on scheduled_at
  | "sent"       // delivered to Resend successfully
  | "opened"     // prospect opened (Resend webhook)
  | "clicked"    // prospect clicked a link (Resend webhook)
  | "replied"    // prospect replied (Gmail polling)
  | "bounced"    // hard or soft bounce (Resend webhook)
  | "discarded"; // human chose not to send this email

export type SequenceDay = 1 | 4 | 7;

// ── Core Model ─────────────────────────────────────────────────────────────

export interface Email {
  id: string;              // UUID
  campaign_id: string;     // UUID
  prospect_id: string;     // UUID
  prospect_email: string;  // denormalised for quick display
  prospect_name: string;   // denormalised — "{first_name} {last_name}"

  sequence_day: SequenceDay; // which email in the 3-step sequence
  subject: string;
  body: string;              // plain text; newlines rendered as <br> in preview

  status: EmailStatus;
  resend_email_id: string | null; // returned by Resend after sending

  scheduled_at: string | null;  // ISO 8601 — 9 AM prospect local time
  sent_at: string | null;       // ISO 8601
  opened_at: string | null;     // ISO 8601
  replied_at: string | null;    // ISO 8601

  created_at: string;           // ISO 8601
}

// ── Update Request ─────────────────────────────────────────────────────────

export interface UpdateEmailRequest {
  subject?: string;
  body?: string;
}

// ── Grouped View (used on Campaign Detail screen) ─────────────────────────

export interface ProspectEmailGroup {
  prospect: import("./prospect").Prospect;
  emails: Email[]; // always sorted: Day 1 → Day 4 → Day 7
}

export interface CampaignEmailsResponse {
  campaign_id: string;
  grouped_by_prospect: ProspectEmailGroup[];
}

// ── UI Helpers ─────────────────────────────────────────────────────────────

/** True if the email can still be edited (not yet sent) */
export const isEditable = (status: EmailStatus): boolean =>
  status === "draft" || status === "scheduled";

/** True if the email can be approved (only drafts can be approved) */
export const isApprovable = (status: EmailStatus): boolean =>
  status === "draft";

/** True if the email has been actioned (no further action needed) */
export const isTerminal = (status: EmailStatus): boolean =>
  ["sent", "opened", "clicked", "replied", "bounced", "discarded"].includes(status);

